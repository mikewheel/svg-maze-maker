window.onload = function() {

    log("Setting up the SVG...");
    let SVG_CANVAS_WIDTH = CANVAS_WIDTH_INCHES * DENSITY * CELL_WIDTH_PIXELS;
    let SVG_CANVAS_HEIGHT = CANVAS_HEIGHT_INCHES * DENSITY * CELL_WIDTH_PIXELS;
    document.getElementById("canvas").setAttribute("width", SVG_CANVAS_WIDTH);
    document.getElementById("canvas").setAttribute("height", SVG_CANVAS_HEIGHT);
    document.getElementById("canvas").style.border = "solid 1px black";

    log("Generating the set of excluded cells...");
    let EXCLUDE_INCHES = [];
    for (let x = 0; x < CANVAS_WIDTH_INCHES; x++) {
        if (!(EXCLUSIONS[x].length)) continue;
        for (let y = 0; y < CANVAS_HEIGHT_INCHES; y++) {
            if (EXCLUSIONS[x][y]) EXCLUDE_INCHES.push(new Coordinate(x, y, "inches"));
        }
    }
    let EXCLUDE_CELLS = {};  // Create a hash for constant-time lookup of excluded cells
    // And populate that hash using a for loop over EXCLUDE_INCHES and the DENSITY
    for (let coord of EXCLUDE_INCHES) {
        for (let i = 0; i < DENSITY; i++) {
            for (let j = 0; j < DENSITY; j++) {
                let translatedCoord = new Coordinate(coord.x * DENSITY + i,
                    coord.y * DENSITY + j,
                    "cells");
                EXCLUDE_CELLS[translatedCoord] = true;
            }
        }
    }

    log("Setting up the graph...");
    let GRAPH = new Graph();
    log("Creating the nodes...");
    for (let x = 0; x < (CANVAS_WIDTH_INCHES * DENSITY); x++) {
        for (let y = 0; y < (CANVAS_HEIGHT_INCHES * DENSITY); y++) {
            let coordKey = new Coordinate(x, y, "cells");
            if (!(EXCLUDE_CELLS.hasOwnProperty(coordKey))) {
                GRAPH.addNode(new Node(x, y));
            }
        }
    }
    log("Creating the edges...");
    for (let node of GRAPH.getAllNodes()) {
        // Generate the coordinates of all possible neighbors (some of which may be in EXCLUDE_CELLS)
        let neighborCoords = node.getNeighboringCoords(CANVAS_WIDTH_INCHES * DENSITY, CANVAS_HEIGHT_INCHES * DENSITY);
        for (let coord of neighborCoords) {
            if (GRAPH.hasNode(coord.toString()) &&
                !(GRAPH.hasEdge(node, coord.toString()))) {
                let neighbor = GRAPH.getNode(coord);
                GRAPH.addEdge(node, neighbor);
            }
        }
    }

    log("Generating the maze...");
    let TREE = doKruskals(GRAPH);

    log("Rendering the maze...");
    clearCanvas();
    viewMazeSVG(TREE);

    if (BLACKOUT) {
        blackOutExclusionsSVG(EXCLUSIONS);
    } else {
        borderExclusionsSVG(EXCLUSIONS);
    }
    log("Done!");
};
